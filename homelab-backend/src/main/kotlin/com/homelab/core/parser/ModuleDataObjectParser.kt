package com.homelab.core.parser

import com.homelab.sdk.helper.AppLogger
import com.homelab.core.service.AppletService
import org.w3c.dom.Node
import javax.xml.parsers.DocumentBuilderFactory
import org.w3c.dom.Element
import com.homelab.sdk.data.*

class ModuleDataObjectParser : Parser<TableDefinition> {
    private val log = AppLogger.loggerFor(ModuleDataObjectParser::class)

    override fun parse(element: Element, moduleId: String?): TableDefinition {
        val name = parseObjectName(element)

        val columns = mutableListOf<ColumnDefinition>()
        val autoGeneratedColumns = mutableListOf<AutoGeneratedColumn>()
        val relations = mutableListOf<RelationDefinition>()
        val uniqueTogether = mutableListOf<UniqueTogetherDefinition>()

        element.directChildren().forEach { child ->
            try {
                when (child.tagName.lowercase()) {
                    "autogenerated", "auto-generated", "autogen" ->
                        autoGeneratedColumns.addAll(parseAutoGenerated(child))

                    "col", "column" ->
                        parseColumn(child)?.let(columns::add)

                    "linksto" ->
                        parseRelation(child, moduleId)?.let(relations::add)

                    "constraints" ->
                        uniqueTogether.addAll(parseConstraints(child))

                    else ->
                        log.warn("[ModuleDataObjectParser] Skipping unknown child tag: ${child.tagName} in table: $name")
                }
            } catch (e: Exception) {
                log.error("[ModuleDataObjectParser] Exception while parsing '${child.tagName}' in '$name': ${e.message}", e)
            }
        }

        return TableDefinition(
            name = name,
            columns = columns,
            autoGeneratedColumns = autoGeneratedColumns,
            relations = relations,
            uniqueTogether = uniqueTogether
        )
    }

    private fun parseObjectName(element: Element): String {
        var name = element.tagName

        if (name.equals("object", true) || name.equals("table", true)) {
            val namingVal = element.firstChildElement("naming")?.attr("val")
            if (!namingVal.isNullOrBlank()) name = namingVal
        }

        return name
    }

    private fun parseAutoGenerated(element: Element): List<AutoGeneratedColumn> {
        return element.directChildren().mapNotNull { child ->
            when (child.tagName.lowercase()) {
                "id" -> AutoGeneratedColumn(AutoGeneratedType.ID)
                "createdat", "created_at", "created-at", "created" ->
                    AutoGeneratedColumn(AutoGeneratedType.CREATED_AT)
                "updatedat", "updated_at", "updated-at", "updated" ->
                    AutoGeneratedColumn(AutoGeneratedType.UPDATED_AT)
                "file" ->
                    AutoGeneratedColumn(AutoGeneratedType.FILE)
                else -> {
                    log.warn("[ModuleDataObjectParser] Unknown autogenerated child tag: ${child.tagName}")
                    null
                }
            }
        }
    }

    private fun parseColumn(element: Element): ColumnDefinition? {
        val name = element.firstChildElement("naming")?.attr("val")

        if (name.isNullOrBlank()) {
            log.warn("[ModuleDataObjectParser] Missing naming for column; skipping")
            return null
        }

        val typingElement = element.firstChildElement("typing")
        val type = DataMapper.mapToColumnTyping(typingElement?.attr("val") ?: "string")

        val unique = element.firstChildElement("unique")?.attr("val")?.toBooleanStrictOrNull() ?: false
        val nullable = element.firstChildElement("nullable")?.attr("val")?.toBooleanStrictOrNull() ?: true

        val regex = typingElement?.firstChildElement("regex")?.attr("val")
            ?: element.firstChildElement("regex")?.attr("val")

        return ColumnDefinition(name, type, unique, nullable, regex)
    }

    private fun parseRelation(element: Element, moduleId: String?): RelationDefinition? {
        val moduleName = element.firstChildText("moduleName")
            ?: moduleId
            ?: throw IllegalArgumentException("moduleName is required for linksTo when moduleId is not provided")

        val targetObject = element.firstChildText("targetObject")
            ?: return null

        val cardinalityText = element.firstChildText("cardinality")
            ?: return null

        val cardinality = when (cardinalityText.lowercase()) {
            "one-to-one" -> Cardinality.ONE_TO_ONE
            "one-to-many" -> Cardinality.ONE_TO_MANY
            "many-to-one" -> Cardinality.MANY_TO_ONE
            "many-to-many" -> Cardinality.MANY_TO_MANY
            else -> throw IllegalArgumentException("Unknown cardinality: $cardinalityText")
        }

        val cascadeDelete = element.firstChildText("cascadeDelete")?.toBooleanStrictOrNull() ?: false
        val cascadeUpdate = element.firstChildText("cascadeUpdate")?.toBooleanStrictOrNull() ?: false

        return RelationDefinition(
            targetObject = moduleName,
            targetTable = targetObject,
            cardinality = cardinality,
            cascadeDelete = cascadeDelete,
            cascadeUpdate = cascadeUpdate
        )
    }

    private fun parseConstraints(element: Element): List<UniqueTogetherDefinition> {
        return element.directChildren()
            .filter { it.tagName.equals("uniqueTogether", ignoreCase = true) }
            .mapNotNull { uniqueElement ->
                val fields = uniqueElement.directChildren()
                    .filter { it.tagName.equals("field", ignoreCase = true) }
                    .mapNotNull { it.textContent?.trim() }
                    .filter { it.isNotBlank() }

                if (fields.size < 2) {
                    log.warn("[ModuleDataObjectParser] uniqueTogether requires at least 2 fields")
                    return@mapNotNull null
                }

                UniqueTogetherDefinition(
                    name = uniqueElement.attr("name"),
                    fields = fields
                )
            }
    }

    private fun Element.directChildren(): List<Element> {
        val result = mutableListOf<Element>()
        val nodes = childNodes

        for (i in 0 until nodes.length) {
            val node = nodes.item(i)
            if (node.nodeType == Node.ELEMENT_NODE) {
                result.add(node as Element)
            }
        }

        return result
    }

    private fun Element.firstChildElement(tagName: String): Element? {
        return directChildren().firstOrNull {
            it.tagName.equals(tagName, ignoreCase = true)
        }
    }

    private fun Element.firstChildText(tagName: String): String? {
        return firstChildElement(tagName)?.textContent?.trim()?.takeIf { it.isNotBlank() }
    }

    private fun Element.attr(name: String): String? {
        return attributes?.getNamedItem(name)?.nodeValue?.trim()?.takeIf { it.isNotBlank() }
    }

    companion object {
        fun parseFromXml(xml: String, moduleId: String?): TableDefinition {
            val factory = DocumentBuilderFactory.newInstance()
            val builder = factory.newDocumentBuilder()
            val document = builder.parse(xml.byteInputStream())
            return ModuleDataObjectParser().parse(document.documentElement, moduleId)
        }
    }
}
