    override fun queryRows(moduleId: String, tableName: String, filters: Map<String, Any?>): List<Map<String, Any?>> {
        val schemaName = safeSqlName(moduleId)
        val tblName = safeSqlName(tableName)
        val results = mutableListOf<Map<String, Any?>>()

        val tableDefinition = objectDefinitionMemory.getDefinition(moduleId, tableName)
        if (tableDefinition == null) {
            println("[ModuleDatabaseService] queryRows: tableDefinition not found for moduleId: $moduleId, tableName: $tableName")
            return results
        }
//        println("[ModuleDatabaseService] queryRows: tableDefinition: $tableDefinition")
        try {
            dataSource.connection.use { connection ->
                val (where, params) = buildWhereClause(filters)
                val sql = "SELECT * FROM \"$schemaName\".\"$tblName\"$where"
                println("[ModuleDatabaseService] Querying rows from \"$schemaName\".\"$tblName\": $sql with values: ${params.joinToString(", ")}")

                val baseRows = mutableListOf<Map<String, Any?>>()

                connection.prepareStatement(sql).use { stmt ->
                    for (i in params.indices) stmt.setObject(i + 1, params[i])
                    stmt.executeQuery().use { rs ->
                        val md = rs.metaData
                        while (rs.next()) {
                            val row = mutableMapOf<String, Any?>()
                            for (i in 1..md.columnCount) {
                                val colName = md.getColumnLabel(i)
                                row[colName] = rs.getObject(i)
                            }
                            baseRows.add(row)
                        }
                    }
                }
//                println("[ModuleDatabaseService] queryRows: baseRows: $baseRows")

                if (tableDefinition.relations.isEmpty()) {
                    return baseRows
                }
                // add eager relations to baseRows


                return loadRelations(
                    connection,
                    schemaName,
                    tblName,
                    tableDefinition,
                    baseRows
                )
            }
        } catch (e: Exception) {
            println("Failed querying rows from \"$schemaName\".\"$tblName\": ${e.message}")
        }
        return results
    }

