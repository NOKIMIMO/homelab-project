    private fun loadRelations(
        connection: Connection,
        schema: String,
        table: String,
        definition: TableDefinition,
        rows: List<Map<String, Any?>>
    ): List<Map<String, Any?>> {

        val result = rows.map { it.toMutableMap() }

        val baseIds = rows.mapNotNull { it["id"] }

        for (relation in definition.relations) {

            when (relation.cardinality) {
                Cardinality.MANY_TO_MANY -> {

//                    println("[ModuleDatabaseService] MANY_TO_MANY cardinality for relation: $relation")

                    val joinTable = listOf(table, relation.targetTable)
                        .sorted()
                        .joinToString("_")

                    val fkSource = "${table}_id"
                    val fkTarget = "${relation.targetTable}_id"

                    val placeholders = baseIds.joinToString(",") { "?" }

                    val sql = """
                    SELECT * FROM "$schema"."$joinTable"
                    WHERE "$fkSource" IN ($placeholders)
                """.trimIndent()

//                    println("[ModuleDatabaseService] SQL for MANY_TO_MANY cardinality: $sql")
                    val map = mutableMapOf<Any, MutableList<Map<String, Any?>>>()

                    connection.prepareStatement(sql).use { stmt ->
                        baseIds.forEachIndexed { i, id ->
                            stmt.setObject(i + 1, id)
                        }

                        stmt.executeQuery().use { rs ->
                            while (rs.next()) {

                                val parentId = rs.getObject(fkSource)
                                val targetId = rs.getObject(fkTarget)

                                val targetRow = fetchSingleRow(
                                    connection,
                                    schema,
                                    relation.targetTable,
                                    targetId
                                )

                                if (targetRow != null) {
                                    map.computeIfAbsent(parentId) { mutableListOf() }
                                        .add(targetRow)
                                }
                            }
                        }
                    }

                    result.forEach { row ->
                        val id = row["id"]
                        row[relation.targetTable] = map[id] ?: emptyList<Any>()
                    }
                }
                Cardinality.MANY_TO_ONE -> {

                    val fk = "${relation.targetTable}_id"

                    val targetIds = rows.mapNotNull { it[fk] }.distinct()

                    val targets = fetchBatch(
                        connection,
                        schema,
                        relation.targetTable,
                        targetIds
                    )

                    val targetMap = targets.associateBy { it["id"] }

                    result.forEach { row ->
                        val fkValue = row[fk]
                        row[relation.targetTable] = targetMap[fkValue]
                    }
                }
                Cardinality.ONE_TO_MANY -> {

                    val fk = "${table}_id"

                    val children = fetchChildren(
                        connection,
                        schema,
                        relation.targetTable,
                        fk,
                        baseIds
                    )

                    val grouped = children.groupBy { it[fk] }

                    result.forEach { row ->
                        val id = row["id"]
                        row[relation.targetTable] = grouped[id] ?: emptyList<Any>()
                    }
                }
                Cardinality.ONE_TO_ONE -> {

                    val fk = "${relation.targetTable}_id"

                    val targetIds = rows.mapNotNull { it[fk] }.distinct()

                    val targets = fetchBatch(
                        connection,
                        schema,
                        relation.targetTable,
                        targetIds
                    )

                    val map = targets.associateBy { it["id"] }

                    result.forEach { row ->
                        val fkValue = row[fk]
                        row[relation.targetTable] = map[fkValue]
                    }
                }
            }
        }

        return result
    }
    private fun fetchBatch(
        connection: Connection,
        schema: String,
        table: String,
        ids: List<Any>
    ): List<Map<String, Any?>> {

        if (ids.isEmpty()) return emptyList()

        val placeholders = ids.joinToString(",") { "?" }

        val sql = """SELECT * FROM "$schema"."$table" WHERE id IN ($placeholders)"""

        val result = mutableListOf<Map<String, Any?>>()

        connection.prepareStatement(sql).use { stmt ->
            ids.forEachIndexed { i, id ->
                stmt.setObject(i + 1, id)
            }

            stmt.executeQuery().use { rs ->
                val md = rs.metaData

                while (rs.next()) {
                    val row = mutableMapOf<String, Any?>()

                    for (i in 1..md.columnCount) {
                        row[md.getColumnLabel(i)] = rs.getObject(i)
                    }

                    result.add(row)
                }
            }
        }

        return result
    }
    private fun fetchSingleRow(
        connection: Connection,
        schema: String,
        table: String,
        id: Any
    ): Map<String, Any?>? {

        val sql = """SELECT * FROM "$schema"."$table" WHERE id = ?"""

        connection.prepareStatement(sql).use { stmt ->
            stmt.setObject(1, id)

            stmt.executeQuery().use { rs ->
                if (!rs.next()) return null

                val md = rs.metaData
                val row = mutableMapOf<String, Any?>()

                for (i in 1..md.columnCount) {
                    row[md.getColumnLabel(i)] = rs.getObject(i)
                }

                return row
            }
        }
    }
    private fun fetchChildren(
        connection: Connection,
        schema: String,
        table: String,
        foreignKey: String,
        parentIds: List<Any>
    ): List<Map<String, Any?>> {

        if (parentIds.isEmpty()) return emptyList()

        val placeholders = parentIds.joinToString(",") { "?" }

        val sql = """
        SELECT * FROM "$schema"."$table"
        WHERE "$foreignKey" IN ($placeholders)
    """.trimIndent()

        val result = mutableListOf<Map<String, Any?>>()

        connection.prepareStatement(sql).use { stmt ->
            parentIds.forEachIndexed { i, id ->
                stmt.setObject(i + 1, id)
            }

            stmt.executeQuery().use { rs ->
                val md = rs.metaData

                while (rs.next()) {
                    val row = mutableMapOf<String, Any?>()

                    for (i in 1..md.columnCount) {
                        row[md.getColumnLabel(i)] = rs.getObject(i)
                    }

                    result.add(row)
                }
            }
        }

        return result
    }
