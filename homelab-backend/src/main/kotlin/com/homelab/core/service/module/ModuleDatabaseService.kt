    override fun updateRows(
        moduleId: String,
        tableName: String,
        filters: Map<String, Any?>,
        updateMap: Map<String, Any?>
    ): Int {
        val schemaName = safeSqlName(moduleId)
        val tblName = safeSqlName(tableName)

        if (updateMap.isEmpty()) {
            println("updateRows: updateMap is empty for \"$schemaName\".\"$tblName\"")
            return 0
        }

        try {
            dataSource.connection.use { connection ->
                // Build SET clause
                val setClauses = mutableListOf<String>()
                val setParams = mutableListOf<Any?>()

                for ((k, v) in updateMap) {
                    val col = safeSqlName(k)
                    setClauses.add("\"$col\" = ?")
                    setParams.add(v)
                }

                // Build WHERE clause
                val (where, whereParams) = buildWhereClause(filters)
                if (where.isEmpty()) {
                    println("updateRows: no filters provided, skipping update for safety on \"$schemaName\".\"$tblName\"")
                    return 0
                }

                val setClause = setClauses.joinToString(", ")
                val sql = "UPDATE \"$schemaName\".\"$tblName\" SET $setClause$where"

                println("Updating rows in \"$schemaName\".\"$tblName\": $sql")
                println("  SET params: ${setParams.joinToString(", ")}")
                println("  WHERE params: ${whereParams.joinToString(", ")}")

                connection.prepareStatement(sql).use { stmt ->
                    var paramIndex = 1

                    // Bind SET parameters
                    for (param in setParams) {
                        stmt.setObject(paramIndex++, param)
                    }

                    // Bind WHERE parameters
                    for (param in whereParams) {
                        stmt.setObject(paramIndex++, param)
                    }

                    return stmt.executeUpdate()
                }
            }
        } catch (e: Exception) {
            println("Failed updating rows in \"$schemaName\".\"$tblName\": ${e.message}")
            e.printStackTrace()
        }
        return 0
    }

